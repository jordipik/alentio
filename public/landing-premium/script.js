const mobileMenuBtn = document.getElementById('mobileMenuBtn');
const mobileMenu = document.getElementById('mobileMenu');
const mainHeader = document.getElementById('mainHeader');
const yearElement = document.getElementById('year');
const langDesktop = document.getElementById('langDesktop');
const langMobile = document.getElementById('langMobile');

function getValueByPath(objectValue, path) {
    return path.split('.').reduce((acc, key) => {
        if (acc === undefined || acc === null) return undefined;
        if (/^\d+$/.test(key)) return acc[Number(key)];
        return acc[key];
    }, objectValue);
}

function applyLanguage(language) {
    const t = translations[language] || translations.es;

    document.documentElement.setAttribute('lang', language);
    document.title = t.seo.title;

    const descriptionMeta = document.getElementById('seo-description');
    const ogTitleMeta = document.getElementById('seo-og-title');
    const ogDescriptionMeta = document.getElementById('seo-og-description');

    if (descriptionMeta) descriptionMeta.setAttribute('content', t.seo.description);
    if (ogTitleMeta) ogTitleMeta.setAttribute('content', t.seo.title);
    if (ogDescriptionMeta) ogDescriptionMeta.setAttribute('content', t.seo.ogDescription);

    document.querySelectorAll('[data-i18n]').forEach((node) => {
        const key = node.getAttribute('data-i18n');
        if (!key) return;
        const translated = getValueByPath(t, key);
        if (translated === undefined) return;
        node.textContent = translated;
    });

    document.querySelectorAll('[data-i18n-aria-label]').forEach((node) => {
        const key = node.getAttribute('data-i18n-aria-label');
        if (!key) return;
        const translated = getValueByPath(t, key);
        if (translated === undefined) return;
        node.setAttribute('aria-label', translated);
    });

    if (langDesktop) langDesktop.value = language;
    if (langMobile) langMobile.value = language;
    localStorage.setItem('alentio_lang', language);
}

if (yearElement) {
    yearElement.textContent = String(new Date().getFullYear());
}

if (langDesktop) {
    langDesktop.addEventListener('change', (event) => {
        applyLanguage(event.target.value);
    });
}

if (langMobile) {
    langMobile.addEventListener('change', (event) => {
        applyLanguage(event.target.value);
    });
}

const storedLanguage = localStorage.getItem('alentio_lang') || 'es';
applyLanguage(storedLanguage);

if (mobileMenuBtn && mobileMenu) {
    mobileMenuBtn.addEventListener('click', () => {
        mobileMenu.classList.toggle('is-open');
    });

    mobileMenu.querySelectorAll('a').forEach((link) => {
        link.addEventListener('click', () => {
            mobileMenu.classList.remove('is-open');
        });
    });
}

window.addEventListener('scroll', () => {
    if (!mainHeader) return;

    if (window.scrollY > 10) {
        mainHeader.classList.add('border-stone-200', 'bg-white/80');
    } else {
        mainHeader.classList.remove('border-stone-200', 'bg-white/80');
    }
});

const revealElements = document.querySelectorAll('.reveal');
const revealObserver = new IntersectionObserver(
    (entries, observer) => {
        entries.forEach((entry) => {
            if (entry.isIntersecting) {
                entry.target.classList.add('is-visible');
                observer.unobserve(entry.target);
            }
        });
    },
    {
        root: null,
        rootMargin: '0px 0px -12% 0px',
        threshold: 0.15,
    }
);

revealElements.forEach((element) => revealObserver.observe(element));

const sectionIds = ['quienes', 'servicios', 'proyectos', 'proceso', 'ambitos', 'contacto'];
const desktopLinks = document.querySelectorAll('.nav-link');
const sectionElements = sectionIds
    .map((id) => document.getElementById(id))
    .filter(Boolean);

const sectionObserver = new IntersectionObserver(
    (entries) => {
        entries.forEach((entry) => {
            if (entry.isIntersecting) {
                const currentId = entry.target.getAttribute('id');
                desktopLinks.forEach((link) => {
                    const href = link.getAttribute('href') || '';
                    const isCurrent = href === `#${currentId}`;
                    link.classList.toggle('is-active', isCurrent);
                });
            }
        });
    },
    {
        root: null,
        threshold: 0.4,
    }
);

sectionElements.forEach((section) => sectionObserver.observe(section));
